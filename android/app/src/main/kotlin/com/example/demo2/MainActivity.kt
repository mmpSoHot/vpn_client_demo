            android.util.Log.d("MainActivity", "‚úÖ .srs Êñá‰ª∂Â§çÂà∂ÂÆåÊàê")
            
            android.util.Log.d("MainActivity", "‚öôÔ∏è Ë∞ÉÁî® Libbox.setup()...")
